#!/bin/bash
# Vibe Dev v6 — stuck-signal reminder (вызывается hooks/dispatch-user-prompt.sh на UserPromptSubmit).
# Анти-залипание, прокси №1 разбора [[tunnel-vision-goal-substitution]] (ретроспектива 2026-06-05).
#
# Промпт пользователя содержит стоп-сигнал / коррекцию курса («не туда / остановись /
# мы не то делаем / дай руками / ты залип») -> печатает напоминание, которое диспетчер
# инжектит модели (additionalContext): стоп-сигнал = смена УРОВНЯ, не способа; проверить
# валидность под-цели + запустить субагент-диагностику, а НЕ ещё одну тактическую попытку.
#
# Почему не block: промпт легитимен; и распознать ложную под-цель по СМЫСЛУ хука нельзя
# (нет pre-final-message события в SDK). Это машинный ПРОКСИ на проигнорированный
# стоп-сигнал — наивысший ROI по разбору (4 сигнала пользователя были обработаны
# тактически, не стратегически). warn/inject, НЕ block. Печатает текст или пусто. exit 0.
#
# 3-criterion gate: docs/anti-stuck-gate-2026-06-05.md (Гипотеза 1 — APPROVE).
# ⚠️ Имя поля промпта в payload — fallback (как handoff-reminder); живой тест подтверждает.

set -u
CWD="${1:-$PWD}"

# Текст промпта: те же имена поля, что у handoff-reminder (fallback).
PROMPT="$(printf '%s' "${HOOK_PAYLOAD:-}" | jq -r '.prompt // .user_prompt // .message // .content // empty' 2>/dev/null)"
[ -z "$PROMPT" ] && exit 0

# Словарь стоп-сигналов (документированные фразы из [[tunnel-vision-goal-substitution]] +
# высокоувернные «залип/застрял»). Заглавная первой буквы — через char-class (без grep -i:
# фолдинг кириллицы в BSD grep ненадёжен). Одиночные общие слова («стоп», «хватит») НЕ
# включены — самотриггер/false-positive; их несут составные фразы.
SIGNALS='[Нн]е туда|не в ту сторону|не в том направлении|не тем пут|[Оо]станов(ись|имся)|[Мм]ы не то|не то (делаем|делать)|не та задача|не ту задачу|не тем (заняты|занимаемся)|[Дд]ай( я| мне)?( сам| сама)? руками|я сам(а)? сделаю руками|[Тт]ы залип|[Тт]ы застрял|[Зз]алип на|[Зз]астрял на|не так делаешь|идёшь не так|мы не туда|это не то'

printf '%s' "$PROMPT" | grep -qE "$SIGNALS" || exit 0

cat <<'TXT'
⚠️ Похоже на стоп-сигнал / коррекцию курса от пользователя. Обработай это как смену УРОВНЯ, а не способа:
1. НЕ меняй инструмент ещё раз — останови текущий подход.
2. Проверь, нужна ли вообще текущая под-цель, или исходная задача уже решается рядом. Инвентаризуй работающее (ps/ls/grep по конфигам) ПРЕЖДЕ чем строить новый обход.
3. Запусти субагент-диагностику (Task tool, Sonnet/Opus) на структурное решение — параллельно, а не ещё одна тактическая попытка.
Маркер залипания: в ходе появляется новый ИНСТРУМЕНТ, но не новый ВОПРОС о валидности цели. Минимум один следующий ход должен сменить ВОПРОС, не инструмент.
TXT
exit 0
