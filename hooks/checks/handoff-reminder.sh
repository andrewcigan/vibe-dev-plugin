#!/bin/bash
# Vibe Dev v6 — handoff reminder (вызывается hooks/dispatch-user-prompt.sh на UserPromptSubmit). H6.
#
# Промпт пользователя содержит сигнал завершения сессии («закрываем / на сегодня всё /
# стартуй новую сессию») -> печатает текст mental cold-start чеклиста, который диспетчер
# инжектит модели (additionalContext). Цель: план уходит в файлы, не остаётся в чате.
# Закрывает паттерн «handoff через слова» (план остаётся в чате, не в файлах).
#
# Печатает на stdout текст напоминания (многострочный) или пусто (нет сигнала). Всегда exit 0.
# Это context-проверка (печатает сырой текст), НЕ block-проверка (которые печатают VERDICT<TAB>msg).
#
# ⚠️ Имя поля промпта в UserPromptSubmit payload НЕ верифицировано на живом — читаем с
# fallback. Живая проверка (рестарт) должна подтвердить реальное поле и формат additionalContext.

set -u
CWD="${1:-$PWD}"

# Текст промпта: пробуем известные/вероятные имена поля.
PROMPT="$(printf '%s' "${HOOK_PAYLOAD:-}" | jq -r '.prompt // .user_prompt // .message // .content // empty' 2>/dev/null)"
[ -z "$PROMPT" ] && exit 0

# Словарь сигналов завершения (из триггеров ~/CLAUDE.md next-session + skills/end-session).
KEYWORDS='[Зз]акрываем|на сегодня (всё|все|хватит)|продолж(им|аем) в новой|старту[йем][^ ]* новую сесси|откро[йем][^ ]* новый чат|заканчиваем|до завтра|конец сесси|на сегодня всё'

printf '%s' "$PROMPT" | grep -qE "$KEYWORDS" || exit 0

cat <<'TXT'
⚠️ Похоже на сигнал завершения сессии. Перед финальным ответом выполни mental cold-start test — план передаётся ТОЛЬКО через файлы, не через чат (иначе следующая сессия его не увидит):
1. feature_list.json — новые/изменённые фичи со статусами и verification_command.
2. SESSION.md — раздел «NEXT»: что делать дальше, в каком порядке, как проверять, что готово.
3. memory/*.md (+ MEMORY.md индекс) — если родилось правило/решение для будущих сессий.
4. docs/ — если есть spec/инструкции (OAuth-шаги, API, план новой задачи).
Проверь себя: увидит ли новая сессия твои решения, читая ТОЛЬКО файлы проекта? Если нет — зафиксируй ДО финального ответа. Не пиши «расскажи план в новой сессии» — чат между сессиями не передаётся.
TXT
exit 0
